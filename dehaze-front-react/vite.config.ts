import react from "@vitejs/plugin-react";
import { resolve } from "path";
import UnoCSS from "unocss/vite";

import { ConfigEnv, defineConfig, loadEnv, UserConfig } from "vite";
import mockDevServerPlugin from "vite-plugin-mock-dev-server";
import svgr from "vite-plugin-svgr";
import {
  dependencies,
  devDependencies,
  engines,
  name,
  version,
} from "./package.json";

/** 平台的名称、版本、运行所需的`node`版本、依赖、构建时间的类型提示 */
const __APP_INFO__ = {
  pkg: { name, version, engines, dependencies, devDependencies },
  buildTimestamp: Date.now(),
};

const pathSrc = resolve(__dirname, "src");
// https://vitejs.dev/config/
export default defineConfig(({ mode }: ConfigEnv): UserConfig => {
  const env = loadEnv(mode, process.cwd());
  return {
    resolve: {
      alias: {
        "@": pathSrc,
      },
    },

    server: {
      // 允许IP访问
      host: "0.0.0.0",
      // 应用端口 (默认:3000)
      port: Number(env.VITE_APP_PORT),
      // 运行是否自动打开浏览器
      open: true,
      proxy: {
        /** 代理前缀为 /java-api 的请求  */
        [env.VITE_JAVA_BASE_API]: {
          changeOrigin: true,
          // 接口地址
          target: env.VITE_JAVA_API_URL,
          rewrite: (path) =>
            path.replace(new RegExp("^" + env.VITE_JAVA_BASE_API), ""),
        },
        /** 代理前缀为 /py-api 的请求  */
        [env.VITE_PYTHON_BASE_API]: {
          changeOrigin: true,
          target: env.VITE_PYTHON_API_URL,
          rewrite: (path) =>
            path.replace(new RegExp("^" + env.VITE_PYTHON_BASE_API), ""),
        },
      },
    },
    plugins: [
      react(),
      // MOCK 服务
      env.VITE_MOCK_DEV_SERVER === "true" ? mockDevServerPlugin() : null,
      UnoCSS({
        hmrTopLevelAwait: false,
      }),
      svgr({
        svgrOptions: {
          icon: true, // 将 SVG 转换为 React 组件时自动调整为图标尺寸
          svgProps: { focusable: "false" }, // 为 SVG 添加默认属性
        },
        include: "src/assets/icons/*.svg",
      }),
    ],
    // 构建配置
    build: {
      chunkSizeWarningLimit: 2000, // 消除打包大小超过500kb警告
      minify: "terser", // Vite 2.6.x 以上需要配置 minify: "terser", terserOptions 才能生效
      terserOptions: {
        compress: {
          keep_infinity: true, // 防止 Infinity 被压缩成 1/0，这可能会导致 Chrome 上的性能问题
          drop_console: true, // 生产环境去除 console
          drop_debugger: true, // 生产环境去除 debugger
        },
        format: {
          comments: false, // 删除注释
        },
      },
      rollupOptions: {
        output: {
          // 用于从入口点创建的块的打包输出格式[name]表示文件名,[hash]表示该文件内容hash值
          entryFileNames: "js/[name].[hash].js",
          // 用于命名代码拆分时创建的共享块的输出命名
          chunkFileNames: "js/[name].[hash].js",
          // 用于输出静态资源的命名，[ext]表示文件扩展名
          assetFileNames: (assetInfo: any) => {
            const info = assetInfo.name.split(".");
            let extType = info[info.length - 1];
            // console.log('文件信息', assetInfo.name)
            if (
              /\.(mp4|webm|ogg|mp3|wav|flac|aac)(\?.*)?$/i.test(assetInfo.name)
            ) {
              extType = "media";
            } else if (/\.(png|jpe?g|gif|svg)(\?.*)?$/.test(assetInfo.name)) {
              extType = "img";
            } else if (/\.(woff2?|eot|ttf|otf)(\?.*)?$/i.test(assetInfo.name)) {
              extType = "fonts";
            }
            return `${extType}/[name].[hash].[ext]`;
          },
        },
      },
    },
    define: {
      __APP_INFO__: JSON.stringify(__APP_INFO__),
    },
  };
});
